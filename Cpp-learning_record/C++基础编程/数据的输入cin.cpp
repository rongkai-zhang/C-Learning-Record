#include <iostream>
#include <string>
using namespace std;
int main ()
{
	int a = 0;// 整形
	cout<< "请给整形a赋值"<<endl;
	cin>>a;
	cout <<"整形变量a ="<<a<<endl; 
	float f = 3.14f;//浮点型 
	cout<< "请给浮点型f赋值"<<endl;
	cin>>f;
	cout <<"浮点型变量f ="<<f<<endl; 
	char ch = 'a';//字符型 
	cout<< "请给字符型 ch赋值"<<endl;
	cin>>ch;
	cout <<"字符型变量ch ="<<ch<<endl; 
	string str = "champion";//字符串型 
	cout<< "请给字符串型str赋值"<<endl;
	cin>>str;
	cout <<"字符串型变量str ="<<str<<endl; 
	bool flag = false;
	cout<< "请给布尔型flag赋值"<<endl;
	cin>>flag;
	cout <<"布尔型变量flag ="<<flag<<endl; 
	
	
	
	
	
	return 0;
}
