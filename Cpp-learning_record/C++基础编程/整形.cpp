#include <iostream> 
using namespace std;
int main ()
{
	//短整形 short 2字节 
	short num1 = 32768;
	// 整形 int  4字节 
	int num2 = 10;
	//长整形long  Windows 4字节 Linux 32位4字节 64位8字节 
	long num3 = 10;
	//长长整形 long long 8字节 
	long long num4 = 10;
	cout <<"num1 = "<< num1 <<endl;  
	 cout <<"num2 = "<< num2 <<endl;   
	 cout <<"num3 = "<< num3 <<endl; 
	 cout <<"num4 = "<< num4 <<endl;   
	   return 0;
 } 
